/**
 * Adminhtml newsletter subscribers grid channels item renderer
 *
 * @category   Mediarocks
 * @package    Mediarocks_NewsletterExtended
 * @author     Media Rocks Developer
 */

class Mediarocks_NewsletterExtended_Adminhtml_Block_Newsletter_Subscriber_Grid_Renderer_Channels extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Abstract
{
	public function render(Varien_Object $row)
	{
        // use subscriber data because no customer data for channels available
        $value = $row->getSubscriberChannels();

        return $value ? $value : '---';
	}
}